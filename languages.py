def get_text(lang_code, key):
    """Get translated UI text for a given language and key"""
    if lang_code not in LANGUAGES:
        lang_code = "en"
    return LANGUAGES[lang_code]["ui"].get(key, key)

def get_medical_term(term, lang_code):
    """Get translated medical term"""
    term_lower = term.lower()
    if term_lower in MEDICAL_GLOSSARY and lang_code in MEDICAL_GLOSSARY[term_lower]:
        return MEDICAL_GLOSSARY[term_lower][lang_code]
    return term  # Return original if not found